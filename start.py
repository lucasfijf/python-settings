def soma(num_1: int, num_2: int) -> int:
    """Somando dois numeros
    :param - num_1: primeiro numero da soma
           - num_2: segundo numero da soma
    :return - soma entre dois numeros
    """
    soma_dois_numeros = num_1 + num_2
    return soma_dois_numeros


